a = input('Digite algo: ')

print('O tipo primitivo desse valor é', (type(a)))
print('Só tem espaços?', (a.isspace()))
print('O valor digitado é um número?', (a.isnumeric()))
print('É alfabético?', (a.isalpha()))
print('O valor digitado é alphanúmerico?', (a.isalnum()))
print('O valor digitado está em letras maíusculas?', (a.isupper()))
print('O valor está em minusculas?', (a.islower()))
print('O valor está capitalizado?', (a.istitle()))
print('O valor digitado é uma letra?', (a.isalpha()))






